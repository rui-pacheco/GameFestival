    private void listarDias(){
        DefaultTableModel modeloDias = (DefaultTableModel) tabelaDiasSumario.getModel();
        SimpleDateFormat dataS = new SimpleDateFormat("dd.MM.yy");
        for (Bilheteira bilheteira :  festival.getBilheteiras().getBilheteiras()){
                Object data = dataS.format(bilheteira.getData());
                Object bilhetesVendidos = bilheteira.getBilhetes_vendidos();
                float ganhos = bilheteira.getLucro()+festival.getPatrocinios().getReceitaPatrocinios();
                float custo = festival.getSessoes().getCustoByData(dataS.format(bilheteira.getData()))+festival.getColaboradores().getCusto();
                float lucro = ganhos-custo;
                Object dados [ ]={data,bilhetesVendidos,ganhos,custo,lucro};
                modeloDias.addRow(dados);
        }
                }
    private void listarHideDias(){
        DefaultTableModel modeloSumario = (DefaultTableModel) tabelaDiasSumario.getModel();
        int n = modeloSumario.getRowCount();
        for (int i =  0 ; i < n; i++) {
            modeloSumario.removeRow(0);
        }
    }
    
    private void atualizarSumario(){
        listarHideDias();
        listarDias();
    }
    
    


