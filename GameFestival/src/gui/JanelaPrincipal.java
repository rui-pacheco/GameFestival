    private void comprar(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_comprar
        if (bilhetesAComprar.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Introduzir numero de bilhetes a comprar!");
            bilhetesAComprar.requestFocus();
            return;
        }
        if(Integer.parseInt(bilhetesAComprar.getText())<=0){
            JOptionPane.showMessageDialog(this, "Introduza um valor válido");
            bilhetesAComprar.requestFocus();
            return;
        }
        if (festival.getBilheteiras().getBilheteiras().get(festival.getBilheteiras().getBilheteiras().size()-1).getBilhetes().size()>0){
            festival.getBilheteiras().getBilheteiras().get(festival.getBilheteiras().getBilheteiras().size()-1).compra(Integer.parseInt(bilhetesAComprar.getText()));
            JOptionPane.showMessageDialog(this, "Compra Efetuada com Sucesso!","Compra", JOptionPane.INFORMATION_MESSAGE);
            bilhetesDisponiveis.setText(Integer.toString(festival.getBilheteiras().getBilheteiras().get(festival.getBilheteiras().getBilheteiras().size()-1).getBilhetes().size()));
            SimpleDateFormat data = new SimpleDateFormat("dd.MM.yy");
            bilhetesVendidosBilheteira.setText(Integer.toString(festival.getBilheteiras().getBilheteiras().get(festival.getBilheteiras().getBilheteiras().size()-1).getBilhetes_vendidos()));
            totalNumerarioBilheteira.setText(Float.toString(festival.getBilheteiras().getBilheteiras().get(festival.getBilheteiras().getBilheteiras().size()-1).getLucro()));
        }else{
            //Devolve o controlo da aplicação para o método main
            JOptionPane.showMessageDialog(this, "Introduza um valor válido");
            dispose();
        }
    }//GEN-LAST:event_comprar

